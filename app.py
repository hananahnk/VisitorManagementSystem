        emp_row = employees_df[employees_df['name'] == selected_emp].iloc[0]
        emp_id = emp_row['id']
        emp_status = emp_row['status']

        if st.button("Send Visitor Request"):
            if emp_status != "Available":
                st.warning(f"Employee is {emp_status}. Cannot send request.")
            else:
                add_visitor(v_name, v_phone, v_company, v_purpose, emp_id, visit_datetime)
