import streamlit as st
import sqlite3
from datetime import datetime, timedelta

# ---------- Database Setup ----------
conn = sqlite3.connect('visitor_management.db', check_same_thread=False)
c = conn.cursor()

# Create Employees table
c.execute("""
CREATE TABLE IF NOT EXISTS employees (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    role TEXT NOT NULL,
    availability TEXT DEFAULT 'Available'
)
""")

# Create Visitors table
c.execute("""
CREATE TABLE IF NOT EXISTS visitors (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    phone TEXT,
    company TEXT,
    purpose TEXT,
    employee_id INTEGER,
    employee_name TEXT,
    visit_datetime TEXT,
    exit_time TEXT,
    duration TEXT,
    status TEXT DEFAULT 'Pending'
)
""")
conn.commit()


# ---------- Utility Functions ----------
def add_employee(name, role):
    c.execute("INSERT INTO employees (name, role) VALUES (?, ?)", (name, role))
    conn.commit()

def update_employee(emp_id, name, role, availability):
    c.execute("UPDATE employees SET name=?, role=?, availability=? WHERE id=?", (name, role, availability, emp_id))
    conn.commit()

def delete_employee(emp_id):
    c.execute("DELETE FROM employees WHERE id=?", (emp_id,))
    conn.commit()

def get_employees():
    return c.execute("SELECT * FROM employees").fetchall()

def add_visitor(name, phone, company, purpose, emp_id, emp_name, visit_dt):
    visit_dt_str = visit_dt.strftime("%Y-%m-%d %H:%M:%S") if isinstance(visit_dt, datetime) else str(visit_dt)
    c.execute("""
        INSERT INTO visitors (name, phone, company, purpose, employee_id, employee_name, visit_datetime)
        VALUES (?, ?, ?, ?, ?, ?, ?)
    """, (name, phone, company, purpose, emp_id, emp_name, visit_dt_str))
    conn.commit()

def update_visitor_status(visitor_id, status, new_dt=None):
    if new_dt:
        new_dt_str = new_dt.strftime("%Y-%m-%d %H:%M:%S")
        c.execute("UPDATE visitors SET status=?, visit_datetime=? WHERE id=?", (status, new_dt_str, visitor_id))
    else:
        c.execute("UPDATE visitors SET status=? WHERE id=?", (status, visitor_id))
    conn.commit()

def mark_exit(visitor_id):
    exit_time = datetime.now()
    exit_time_str = exit_time.strftime("%Y-%m-%d %H:%M:%S")
    c.execute("SELECT visit_datetime FROM visitors WHERE id=?", (visitor_id,))
    visit_time_str = c.fetchone()[0]
    visit_time = datetime.strptime(visit_time_str, "%Y-%m-%d %H:%M:%S")
    duration = exit_time - visit_time
    duration_str = str(duration).split('.')[0]  # remove microseconds
    c.execute("UPDATE visitors SET exit_time=?, duration=?, status='Completed' WHERE id=?", (exit_time_str, duration_str, visitor_id))
    conn.commit()


# ---------- Streamlit App ----------
st.set_page_config(page_title="Visitor Management System", layout="wide")
st.title("Visitor Management System")

# --- Role Selection ---
role = st.sidebar.selectbox("Select Role", ["Receptionist", "Employee", "Admin"])

# --- Receptionist ---
if role == "Receptionist":
    st.header("Receptionist Dashboard")

    # Add Employee
    st.subheader("Add Employee")
    e_name = st.text_input("Name")
    e_role = st.text_input("Role")
    if st.button("Add Employee"):
        if e_name and e_role:
            add_employee(e_name, e_role)
            st.success("Employee added successfully")
        else:
            st.error("Enter name and role")

    # Add Visitor
    st.subheader("Register Visitor")
    v_name = st.text_input("Visitor Name")
    v_phone = st.text_input("Phone")
    v_company = st.text_input("Company")
    v_purpose = st.text_input("Purpose")
    visit_dt = st.date_input("Visit Date", datetime.now())
    visit_time = st.time_input("Visit Time", datetime.now().time())

    employees = get_employees()
    emp_options = [f"{emp[0]} - {emp[1]} ({emp[3]})" for emp in employees]
    emp_select = st.selectbox("Assign to Employee", emp_options)

    if st.button("Add Visitor"):
        if employees:
            emp_id = int(emp_select.split(" - ")[0])
            emp_name = emp_select.split(" - ")[1].split("(")[0].strip()
            dt_combined = datetime.combine(visit_dt, visit_time)
            add_visitor(v_name, v_phone, v_company, v_purpose, emp_id, emp_name, dt_combined)
            st.success("Visitor added (Pending Approval)")
        else:
            st.error("No employees available to assign")

    # Upcoming Visits
    st.subheader("Upcoming Visitors")
    visitors = c.execute("SELECT * FROM visitors WHERE status='Pending' OR status='Approved'").fetchall()
    if visitors:
        st.dataframe(visitors)
    else:
        st.write("No upcoming visitors")


# --- Employee ---
elif role == "Employee":
    st.header("Employee Dashboard")
    emp_name_input = st.text_input("Enter Your Name")
    if emp_name_input:
        visitors = c.execute("SELECT * FROM visitors WHERE employee_name=? AND status!='Completed'", (emp_name_input,)).fetchall()
        if visitors:
            for v in visitors:
                st.write(f"Visitor: {v[1]} | Company: {v[3]} | Purpose: {v[4]} | Scheduled: {v[7]}")
                col1, col2, col3 = st.columns(3)
                with col1:
                    if st.button(f"Approve {v[0]}"):
                        update_visitor_status(v[0], "Approved")
                        st.success("Visitor Approved")
                with col2:
                    if st.button(f"Reject {v[0]}"):
                        update_visitor_status(v[0], "Rejected")
                        st.success("Visitor Rejected")
                with col3:
                    new_date = st.date_input(f"Reschedule Date {v[0]}", datetime.now())
                    new_time = st.time_input(f"Reschedule Time {v[0]}", datetime.now().time())
                    if st.button(f"Reschedule {v[0]}"):
                        dt_combined = datetime.combine(new_date, new_time)
                        update_visitor_status(v[0], "Pending", dt_combined)
                        st.success("Visitor Rescheduled")
        else:
            st.write("No pending visitors")


# --- Admin ---
elif role == "Admin":
    st.header("Admin Dashboard")

    # Employee Management
    st.subheader("Manage Employees")
    employees = get_employees()
    for emp in employees:
        st.write(f"ID: {emp[0]} | Name: {emp[1]} | Role: {emp[2]} | Availability: {emp[3]}")
        col1, col2, col3 = st.columns(3)
        with col1:
            new_name = st.text_input(f"Edit Name {emp[0]}", emp[1])
        with col2:
            new_role = st.text_input(f"Edit Role {emp[0]}", emp[2])
        with col3:
            new_avail = st.selectbox(f"Availability {emp[0]}", ["Available", "Not Available"], index=0 if emp[3]=="Available" else 1)
        if st.button(f"Update {emp[0]}"):
            update_employee(emp[0], new_name, new_role, new_avail)
            st.success("Employee Updated")
        if st.button(f"Delete {emp[0]}"):
            delete_employee(emp[0])
            st.warning("Employee Deleted")

    # Visitor Records
    st.subheader("All Visitors")
    visitors = c.execute("SELECT * FROM visitors").fetchall()
    if visitors:
        st.dataframe(visitors)
    else:
        st.write("No visitors recorded")