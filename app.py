import streamlit as st
import sqlite3
import pandas as pd
from datetime import datetime, timedelta

# --- Database Setup ---
conn = sqlite3.connect("vms.db", check_same_thread=False)
c = conn.cursor()

# Create Employees Table
c.execute("""
CREATE TABLE IF NOT EXISTS employees (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    role TEXT NOT NULL,
    availability TEXT DEFAULT 'Available'
)
""")

# Create Visitors Table with exact columns
c.execute("""
CREATE TABLE IF NOT EXISTS visitors (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    phone TEXT,
    company TEXT,
    purpose TEXT,
    employee_id INTEGER,
    employee_name TEXT,
    visit_datetime TEXT,
    status TEXT DEFAULT 'Pending',
    exit_time TEXT,
    duration TEXT
)
""")
conn.commit()

# --- Helper Functions ---
def add_employee(name, role):
    c.execute("INSERT INTO employees (name, role) VALUES (?, ?)", (name, role))
    conn.commit()

def update_employee(emp_id, name, role, availability):
    c.execute("UPDATE employees SET name=?, role=?, availability=? WHERE id=?", (name, role, availability, emp_id))
    conn.commit()

def delete_employee(emp_id):
    c.execute("DELETE FROM employees WHERE id=?", (emp_id,))
    conn.commit()

def add_visitor(name, phone, company, purpose, emp_id, emp_name, visit_dt_str):
    # Ensure visit_dt_str is string
    if isinstance(visit_dt_str, datetime):
        visit_dt_str = visit_dt_str.strftime("%Y-%m-%d %H:%M:%S")
    c.execute("""
        INSERT INTO visitors (name, phone, company, purpose, employee_id, employee_name, visit_datetime)
        VALUES (?, ?, ?, ?, ?, ?, ?)
    """, (name, phone, company, purpose, emp_id, emp_name, visit_dt_str))
    conn.commit()

def update_visitor_status(visitor_id, status):
    c.execute("UPDATE visitors SET status=? WHERE id=?", (status, visitor_id))
    conn.commit()

def mark_exit(visitor_id):
    exit_time = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    c.execute("SELECT visit_datetime FROM visitors WHERE id=?", (visitor_id,))
    visit_time_str = c.fetchone()[0]
    visit_time = datetime.strptime(visit_time_str, "%Y-%m-%d %H:%M:%S")
    duration = str(datetime.now() - visit_time).split('.')[0]
    c.execute("UPDATE visitors SET exit_time=?, duration=?, status='Completed' WHERE id=?", (exit_time, duration, visitor_id))
    conn.commit()

def reschedule_visitor(visitor_id, new_time):
    if isinstance(new_time, datetime):
        new_time = new_time.strftime("%Y-%m-%d %H:%M:%S")
    c.execute("UPDATE visitors SET visit_datetime=?, status='Pending' WHERE id=?", (new_time, visitor_id))
    conn.commit()

# --- Streamlit UI ---
st.title("Smart Visitor Management System")

role = st.sidebar.selectbox("Select Role", ["Receptionist", "Employee", "Admin"])

# Load Data
employees_df = pd.read_sql("SELECT * FROM employees", conn)
visitors_df = pd.read_sql("SELECT * FROM visitors", conn)

# Ensure all columns exist
for col in ['id','name','phone','company','purpose','employee_id','employee_name','visit_datetime','status','exit_time','duration']:
    if col not in visitors_df.columns:
        visitors_df[col] = ""

# --- Receptionist ---
if role == "Receptionist":
    st.header("Receptionist Dashboard")

    st.subheader("Register Visitor")
    v_name = st.text_input("Visitor Name")
    v_phone = st.text_input("Phone")
    v_company = st.text_input("Company")
    v_purpose = st.text_input("Purpose")
    emp_name = st.selectbox("Assign to Employee", employees_df['name'].tolist() if not employees_df.empty else ["No Employee"])
    visit_dt = st.datetime_input("Visit Date & Time", datetime.now() + timedelta(minutes=5))

    if st.button("Add Visitor"):
        if employees_df.empty:
            st.error("No employees available!")
        else:
            emp_row = employees_df[employees_df['name']==emp_name].iloc[0]
            add_visitor(v_name, v_phone, v_company, v_purpose, emp_row['id'], emp_name, visit_dt)
            st.success("Visitor Added (Pending Approval)")
            st.experimental_rerun()

    st.subheader("Upcoming Visitors")
    upcoming_visitors = visitors_df[visitors_df['status']!="Completed"]
    st.dataframe(upcoming_visitors[['id','name','phone','company','purpose','employee_name','visit_datetime','status','exit_time','duration']])

    st.subheader("Mark Exit")
    visitor_to_exit = st.selectbox("Select Visitor to Mark Exit", upcoming_visitors['name'].tolist())
    if st.button("Mark Exit"):
        visitor_id = upcoming_visitors[upcoming_visitors['name']==visitor_to_exit].iloc[0]['id']
        mark_exit(visitor_id)
        st.success("Exit marked")
        st.experimental_rerun()

# --- Employee ---
elif role == "Employee":
    st.header("Employee Dashboard")
    emp_name_input = st.text_input("Enter Your Name")
    if emp_name_input:
        emp_row = employees_df[employees_df['name']==emp_name_input]
        if not emp_row.empty:
            emp_id = emp_row.iloc[0]['id']
            st.subheader("Pending Visitors")
            pending_visitors = visitors_df[(visitors_df['employee_id']==emp_id) & (visitors_df['status']=='Pending')]
            st.dataframe(pending_visitors[['id','name','phone','company','purpose','visit_datetime','status']])

            for idx, row in pending_visitors.iterrows():
                col1, col2, col3 = st.columns(3)
                with col1:
                    if st.button(f"Approve {row['name']}", key=f"approve{row['id']}"):
                        update_visitor_status(row['id'], "Approved")
                        st.experimental_rerun()
                with col2:
                    if st.button(f"Reject {row['name']}", key=f"reject{row['id']}"):
                        update_visitor_status(row['id'], "Rejected")
                        st.experimental_rerun()
                with col3:
                    new_time = st.time_input(f"Reschedule {row['name']}", datetime.strptime(row['visit_datetime'], "%Y-%m-%d %H:%M:%S").time(), key=f"resched{row['id']}")
                    if st.button(f"Apply Reschedule {row['name']}", key=f"applyresched{row['id']}"):
                        new_datetime = datetime.combine(datetime.today(), new_time)
                        reschedule_visitor(row['id'], new_datetime)
                        st.experimental_rerun()
        else:
            st.warning("Employee not found.")

# --- Admin ---
elif role == "Admin":
    st.header("Admin Dashboard")

    st.subheader("Manage Employees")
    new_name = st.text_input("Employee Name")
    new_role = st.text_input("Role")
    if st.button("Add Employee"):
        add_employee(new_name, new_role)
        st.success("Employee Added")
        st.experimental_rerun()

    st.subheader("Edit/Delete Employees")
    for idx, row in employees_df.iterrows():
        col1, col2, col3, col4 = st.columns(4)
        with col1:
            new_name = st.text_input(f"Name {row['id']}", row['name'], key=f"name{row['id']}")
        with col2:
            new_role = st.text_input(f"Role {row['id']}", row['role'], key=f"role{row['id']}")
        with col3:
            availability = st.selectbox(f"Availability {row['id']}", ["Available","Not Free Today"], index=0, key=f"avail{row['id']}")
        with col4:
            if st.button(f"Update {row['id']}", key=f"update{row['id']}"):
                update_employee(row['id'], new_name, new_role, availability)
                st.success("Employee Updated")
                st.experimental_rerun()
            if st.button(f"Delete {row['id']}", key=f"del{row['id']}"):
                delete_employee(row['id'])
                st.success("Employee Deleted")
                st.experimental_rerun()

    st.subheader("All Visitors")
    st.dataframe(visitors_df[['id','name','phone','company','purpose','employee_name','visit_datetime','status','exit_time','duration']])