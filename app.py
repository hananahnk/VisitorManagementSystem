    st.sidebar.subheader("Employee Management")
    employees_df = get_employees_df()
    with st.sidebar.expander("Add Employee"):
        name = st.text_input("Name", key="add_emp_name")
        role_emp = st.text_input("Role", key="add_emp_role")
        dept = st.text_input("Department", key="add_emp_dept")
        if st.button("Add Employee"):
            if name and role_emp and dept:
                add_employee(name, role_emp, dept)
                st.success(f"Employee {name} added successfully!")
                st.experimental_rerun()

    with st.sidebar.expander("Edit/Delete Employees"):
        for idx, row in employees_df.iterrows():
            with st.expander(f"{row['name']} ({row['role']})"):
                new_name = st.text_input("Name", row['name'], key=f"name{row['id']}")
                new_role = st.text_input("Role", row['role'], key=f"role{row['id']}")
                new_dept = st.text_input("Dept", row['department'], key=f"dept{row['id']}")
                new_status = st.selectbox("Status", ["Available","Busy","Not Free Today"], index=["Available","Busy","Not Free Today"].index(row['status']), key=f"status{row['id']}")
