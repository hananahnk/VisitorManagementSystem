import streamlit as st
import sqlite3
import pandas as pd
from datetime import datetime, timedelta

# ---------------------------
# Database Setup
# ---------------------------
conn = sqlite3.connect("vms.db", check_same_thread=False)
c = conn.cursor()

# Employees table
c.execute('''CREATE TABLE IF NOT EXISTS employees (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    role TEXT,
    department TEXT,
    status TEXT DEFAULT 'Available'
)''')

# Visitors table
c.execute('''CREATE TABLE IF NOT EXISTS visitors (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    phone TEXT,
    company TEXT,
    purpose TEXT,
    employee_id INTEGER,
    visit_date_time TEXT,
    exit_time TEXT,
    duration TEXT,
    status TEXT DEFAULT 'Pending',
    FOREIGN KEY(employee_id) REFERENCES employees(id)
)''')
conn.commit()


# ---------------------------
# Helper Functions
# ---------------------------
def get_employees():
    df = pd.read_sql("SELECT * FROM employees", conn)
    return df

def get_visitors():
    df = pd.read_sql("SELECT * FROM visitors", conn)
    # Map employee names
    emp_df = get_employees()[['id','name']]
    df = df.merge(emp_df, left_on='employee_id', right_on='id', how='left', suffixes=('','_emp'))
    if 'name_emp' in df.columns:
        df.rename(columns={'name_emp':'employee_name'}, inplace=True)
    else:
        df['employee_name'] = ''
    return df

def add_employee(name, role, department):
    c.execute("INSERT INTO employees (name, role, department) VALUES (?, ?, ?)", (name, role, department))
    conn.commit()

def update_employee(emp_id, name, role, department, status):
    c.execute("UPDATE employees SET name=?, role=?, department=?, status=? WHERE id=?",
              (name, role, department, status, emp_id))
    conn.commit()

def delete_employee(emp_id):
    c.execute("DELETE FROM employees WHERE id=?", (emp_id,))
    conn.commit()

def add_visitor(name, phone, company, purpose, employee_id, visit_dt):
    c.execute('''INSERT INTO visitors
                 (name, phone, company, purpose, employee_id, visit_date_time)
                 VALUES (?, ?, ?, ?, ?, ?)''',
              (name, phone, company, purpose, employee_id, visit_dt))
    conn.commit()

def update_visitor_status(visitor_id, status, visit_dt=None):
    if visit_dt:
        c.execute("UPDATE visitors SET status=?, visit_date_time=? WHERE id=?", (status, visit_dt, visitor_id))
    else:
        c.execute("UPDATE visitors SET status=? WHERE id=?", (status, visitor_id))
    conn.commit()

def mark_exit(visitor_id):
    exit_time = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    c.execute("SELECT visit_date_time FROM visitors WHERE id=?", (visitor_id,))
    visit_time = c.fetchone()[0]
    duration = str(datetime.strptime(exit_time, "%Y-%m-%d %H:%M:%S") - datetime.strptime(visit_time, "%Y-%m-%d %H:%M:%S"))
    c.execute("UPDATE visitors SET exit_time=?, duration=?, status='Completed' WHERE id=?", (exit_time, duration, visitor_id))
    conn.commit()


