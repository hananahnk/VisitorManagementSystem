import streamlit as st
import sqlite3
import pandas as pd
from datetime import datetime

st.set_page_config(layout="wide")

# -------------------------
# DB SETUP
# -------------------------
conn = sqlite3.connect("vms.db", check_same_thread=False)
c = conn.cursor()

c.execute("""
CREATE TABLE IF NOT EXISTS employees (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT,
    role TEXT,
    department TEXT,
    status TEXT DEFAULT 'Available'
)
""")

c.execute("""
CREATE TABLE IF NOT EXISTS visitors (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT,
    phone TEXT,
    company TEXT,
    purpose TEXT,
    employee_id INTEGER,
    visit_date_time TEXT,
    exit_time TEXT,
    duration TEXT,
    status TEXT DEFAULT 'Pending'
)
""")

conn.commit()

# -------------------------
# FUNCTIONS
# -------------------------
def get_employees():
    try:
        return pd.read_sql("SELECT * FROM employees", conn)
    except:
        return pd.DataFrame()

def get_visitors():
    try:
        return pd.read_sql("SELECT * FROM visitors", conn)
    except:
        return pd.DataFrame()

def add_employee(name, role, dept):
    c.execute("INSERT INTO employees (name, role, department) VALUES (?, ?, ?)", (name, role, dept))
    conn.commit()

def update_employee(emp_id, name, role, dept, status):
    c.execute("UPDATE employees SET name=?, role=?, department=?, status=? WHERE id=?",
              (name, role, dept, status, emp_id))
    conn.commit()

def delete_employee(emp_id):
    c.execute("DELETE FROM employees WHERE id=?", (emp_id,))
    conn.commit()

def add_visitor(name, phone, company, purpose, emp_id, visit_dt):
    c.execute("""
        INSERT INTO visitors (name, phone, company, purpose, employee_id, visit_date_time)
        VALUES (?, ?, ?, ?, ?, ?)
    """, (name, phone, company, purpose, emp_id, visit_dt))
    conn.commit()

def update_visitor_status(visitor_id, status):
    c.execute("UPDATE visitors SET status=? WHERE id=?", (status, visitor_id))
    conn.commit()

def reschedule_visitor(visitor_id, new_dt):
    c.execute("UPDATE visitors SET visit_date_time=?, status='Pending' WHERE id=?",
              (new_dt, visitor_id))
    conn.commit()

# -------------------------
# UI
# -------------------------
st.title("🏢 Visitor Management System")

role = st.sidebar.selectbox("Select Role", ["Receptionist", "Employee", "Admin"])

employees_df = get_employees()
visitors_df = get_visitors()

# =========================================================
# 🟢 RECEPTIONIST
# =========================================================
if role == "Receptionist":

    col1, col2 = st.columns([2,1])

    # -------- LEFT: VISITOR REG --------
    with col1:
        st.subheader("Register Visitor")

        if not employees_df.empty:
            available = employees_df[employees_df["status"] == "Available"]

            if not available.empty:
                name = st.text_input("Visitor Name")
                phone = st.text_input("Phone")
                company = st.text_input("Company")
                purpose = st.text_input("Purpose")

                emp_name = st.selectbox("Assign Employee", available["name"].tolist())

                date = st.date_input("Visit Date")
                time = st.time_input("Visit Time")

                if st.button("Add Visitor"):
                    emp_id = available[available["name"] == emp_name].iloc[0]["id"]
                    visit_dt = datetime.combine(date, time).strftime("%Y-%m-%d %H:%M:%S")
                    add_visitor(name, phone, company, purpose, emp_id, visit_dt)
                    st.success("Visitor Added (Pending Approval)")
                    st.rerun()
            else:
                st.warning("No Available Employees")
        else:
            st.warning("Add employees first")

    # -------- RIGHT: EMPLOYEE MGMT --------
    with col2:
        st.subheader("Employee Management")

        with st.expander("Add Employee"):
            ename = st.text_input("Name")
            erole = st.text_input("Role")
            edept = st.text_input("Department")

            if st.button("Add Employee"):
                add_employee(ename, erole, edept)
                st.success("Added")
                st.rerun()

        if not employees_df.empty:
            for _, row in employees_df.iterrows():
                with st.expander(row.get("name","Emp")):
                    new_name = st.text_input("Name", row.get("name",""), key=f"n{row['id']}")
                    new_role = st.text_input("Role", row.get("role",""), key=f"r{row['id']}")
                    new_dept = st.text_input("Dept", row.get("department",""), key=f"d{row['id']}")

                    status = st.selectbox("Status",
                        ["Available","Already in a Meeting","Not Free Today"],
                        key=f"s{row['id']}")

                    if st.button("Update", key=f"u{row['id']}"):
                        update_employee(row["id"], new_name, new_role, new_dept, status)
                        st.success("Updated")
                        st.rerun()

                    if st.button("Delete", key=f"x{row['id']}"):
                        delete_employee(row["id"])
                        st.warning("Deleted")
                        st.rerun()

    # -------- DASHBOARD --------
    st.subheader("Visitor Dashboard")
    st.dataframe(visitors_df)


# =========================================================
# 🔵 EMPLOYEE
# =========================================================
elif role == "Employee":

    st.header("Employee Dashboard")

    emp_name = st.text_input("Enter Your Name")

    if emp_name:
        emp = employees_df[employees_df["name"] == emp_name]

        if not emp.empty:
            emp_id = emp.iloc[0]["id"]
            my_visitors = visitors_df[visitors_df["employee_id"] == emp_id]

            st.subheader("Pending Requests")

            pending = my_visitors[my_visitors["status"] == "Pending"]

            for _, row in pending.iterrows():
                st.write(f"👤 {row['name']} | {row['purpose']} | {row['visit_date_time']}")

                col1, col2, col3 = st.columns(3)

                if col1.button("Approve", key=f"a{row['id']}"):
                    update_visitor_status(row["id"], "Approved")
                    st.rerun()

                if col2.button("Reject", key=f"r{row['id']}"):
                    update_visitor_status(row["id"], "Rejected")
                    st.rerun()

                new_date = col3.date_input("New Date", key=f"d{row['id']}")
                new_time = col3.time_input("New Time", key=f"t{row['id']}")

                if col3.button("Reschedule", key=f"res{row['id']}"):
                    new_dt = datetime.combine(new_date, new_time).strftime("%Y-%m-%d %H:%M:%S")
                    reschedule_visitor(row["id"], new_dt)
                    st.rerun()

            st.subheader("Approved Visitors")
            st.dataframe(my_visitors[my_visitors["status"]=="Approved"])

        else:
            st.error("Employee not found")


# =========================================================
# 🔴 ADMIN
# =========================================================
elif role == "Admin":

    st.header("Admin Dashboard")

    # -------- STATS --------
    col1, col2, col3 = st.columns(3)
    col1.metric("Total Visitors", len(visitors_df))
    col2.metric("Approved", len(visitors_df[visitors_df["status"]=="Approved"]))
    col3.metric("Rejected", len(visitors_df[visitors_df["status"]=="Rejected"]))

    # -------- CHARTS --------
    st.subheader("Visitor Status Chart")
    st.bar_chart(visitors_df["status"].value_counts())

    st.subheader("Visitors per Employee")
    st.bar_chart(visitors_df["employee_id"].value_counts())

    # -------- FILTER --------
    emp_filter = st.selectbox("Filter by Employee", ["All"] + employees_df["name"].tolist())

    filtered = visitors_df.copy()

    if emp_filter != "All":
        emp_id = employees_df[employees_df["name"] == emp_filter].iloc[0]["id"]
        filtered = filtered[filtered["employee_id"] == emp_id]

    st.dataframe(filtered)

    st.download_button("Download CSV", filtered.to_csv(index=False), "visitors.csv")