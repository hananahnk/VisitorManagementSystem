            new_name = st.sidebar.text_input(f"Name_{row['id']}", row['name'])
            new_role = st.sidebar.text_input(f"Role_{row['id']}", row['role'])
            new_dept = st.sidebar.text_input(f"Department_{row['id']}", row['department'])
            new_status = st.sidebar.selectbox(f"Status_{row['id']}", ["Available", "Already in a Meeting", "Not Free Today"], index=["Available","Already in a Meeting","Not Free Today"].index(row['status']))

            if st.sidebar.button(f"Update_{row['id']}"):
                update_employee(row['id'], new_name, new_role, new_dept, new_status)
                st.sidebar.success("Employee Updated")
            if st.sidebar.button(f"Delete_{row['id']}"):
                delete_employee(row['id'])
                st.sidebar.success("Employee Deleted")


