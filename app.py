            st.subheader("Visitor History")
            completed = emp_visitors[emp_visitors['status'].isin(["Completed","Rejected"])]
            if not completed.empty:
                st.dataframe(completed[['name','phone','company','purpose','visit_date_time','exit_time','duration','status']])


