st.header("Visitor Dashboard")

if not visitors_df.empty:

    st.dataframe(visitors_df)

    st.subheader("Mark Exit")

    vid = st.selectbox("Select Visitor ID", visitors_df["id"].tolist())

    if st.button("Mark Exit"):
        mark_exit(vid)
        st.success("Exit marked")
        st.rerun()

