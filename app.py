import streamlit as st
import sqlite3
import pandas as pd
from datetime import datetime

# ----------------- Database Setup -----------------
conn = sqlite3.connect("visitor_system.db", check_same_thread=False)
c = conn.cursor()

# Employees
c.execute("""
CREATE TABLE IF NOT EXISTS employees (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT,
    role TEXT,
    department TEXT,
    status TEXT DEFAULT 'Available'
)
""")

# Visitors
c.execute("""
CREATE TABLE IF NOT EXISTS visitors (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT,
    phone TEXT,
    company TEXT,
    purpose TEXT,
    employee_id INTEGER,
    visit_date_time TEXT,
    exit_time TEXT,
    duration TEXT,
    status TEXT DEFAULT 'Pending',
    FOREIGN KEY(employee_id) REFERENCES employees(id)
)
""")
conn.commit()

# ----------------- Functions -----------------
def get_employees():
    return pd.read_sql("SELECT * FROM employees", conn)

def get_visitors():
    return pd.read_sql("""
        SELECT v.id, v.name, v.phone, v.company, v.purpose, e.name AS employee_name,
               v.visit_date_time, v.exit_time, v.duration, v.status
        FROM visitors v LEFT JOIN employees e ON v.employee_id = e.id
        ORDER BY v.visit_date_time DESC
    """, conn)

def add_employee(name, role, dept):
    c.execute("INSERT INTO employees (name, role, department) VALUES (?, ?, ?)", (name, role, dept))
    conn.commit()

def update_employee(emp_id, name, role, dept, status):
    c.execute("UPDATE employees SET name=?, role=?, department=?, status=? WHERE id=?",
              (name, role, dept, status, emp_id))
    conn.commit()

def delete_employee(emp_id):
    c.execute("DELETE FROM employees WHERE id=?", (emp_id,))
    conn.commit()

def add_visitor(name, phone, company, purpose, emp_id, dt):
    c.execute("""INSERT INTO visitors
                 (name, phone, company, purpose, employee_id, visit_date_time)
                 VALUES (?, ?, ?, ?, ?, ?)""",
              (name, phone, company, purpose, emp_id, dt))
    conn.commit()

def update_visitor_status(visitor_id, status, new_dt=None):
    if new_dt:
        c.execute("UPDATE visitors SET status=?, visit_date_time=? WHERE id=?", (status, new_dt, visitor_id))
    else:
        c.execute("UPDATE visitors SET status=? WHERE id=?", (status, visitor_id))
    conn.commit()

def mark_exit(visitor_id):
    exit_time = datetime.now()
    c.execute("SELECT visit_date_time FROM visitors WHERE id=?", (visitor_id,))
    visit_time_str = c.fetchone()[0]
    visit_time = datetime.fromisoformat(visit_time_str)
    duration = str(exit_time - visit_time).split('.')[0]
    c.execute("UPDATE visitors SET exit_time=?, duration=?, status='Completed' WHERE id=?",
              (exit_time.isoformat(), duration, visitor_id))
    conn.commit()

# ----------------- UI -----------------
st.title("Visitor Management System")

role = st.sidebar.selectbox("Select Role", ["Receptionist", "Employee", "Admin"])

# ----------------- Receptionist -----------------
if role == "Receptionist":
    st.header("Receptionist Dashboard")
    employees_df = get_employees()

    # Employee management
    with st.expander("Add Employee"):
        name = st.text_input("Name", key="add_name")
        emp_role = st.text_input("Role", key="add_role")
        dept = st.text_input("Department", key="add_dept")
        if st.button("Add Employee", key="add_emp_btn"):
            if name and emp_role and dept:
                add_employee(name, emp_role, dept)
                st.success(f"Added Employee {name}")

    with st.expander("Edit/Delete Employees"):
        for idx, row in employees_df.iterrows():
            with st.container():
                cols = st.columns([2,2,2,1,1])
                cols[0].text_input("Name", row['name'], key=f"name_{row['id']}")
                cols[1].text_input("Role", row['role'], key=f"role_{row['id']}")
                cols[2].text_input("Dept", row['department'], key=f"dept_{row['id']}")
                status = cols[3].selectbox("Status", ["Available","Busy","Not Free Today"],
                                           index=["Available","Busy","Not Free Today"].index(row['status']),
                                           key=f"status_{row['id']}")
                if cols[4].button("Update", key=f"update_{row['id']}"):
                    update_employee(row['id'],
                                    st.session_state[f"name_{row['id']}"],
                                    st.session_state[f"role_{row['id']}"],
                                    st.session_state[f"dept_{row['id']}"],
                                    status)
                    st.success("Updated!")
                if st.button("Delete", key=f"del_{row['id']}"):
                    delete_employee(row['id'])
                    st.success("Deleted!")

    # Visitor registration
    st.subheader("Register Visitor")
    available_emps = employees_df[employees_df['status']=='Available']
    if available_emps.empty:
        st.warning("No Available Employees")
    else:
        v_name = st.text_input("Visitor Name", key="v_name")
        v_phone = st.text_input("Phone", key="v_phone")
        v_company = st.text_input("Company", key="v_company")
        v_purpose = st.text_input("Purpose", key="v_purpose")
        selected_emp = st.selectbox("Assign Employee", available_emps['name'], key="assign_emp")
        visit_dt = st.datetime_input("Visit Date & Time", datetime.now(), key="visit_dt")
        if st.button("Add Visitor", key="add_vis_btn"):
            emp_id = employees_df[employees_df['name']==selected_emp]['id'].values[0]
            add_visitor(v_name, v_phone, v_company, v_purpose, emp_id, visit_dt.isoformat())
            st.success("Visitor Added (Pending Approval)")

    # Mark exit
    st.subheader("Mark Exit")
    visitors_df = get_visitors()
    approved_visitors = visitors_df[visitors_df['status']=='Approved']
    if not approved_visitors.empty:
        exit_choice = st.selectbox("Select Visitor to mark Exit", approved_visitors['id'])
        if st.button("Mark Exit", key="mark_exit_btn"):
            mark_exit(exit_choice)
            st.success("Exit Marked & Duration Calculated")

# ----------------- Employee -----------------
elif role == "Employee":
    st.header("Employee Dashboard")
    employees_df = get_employees()
    emp_name = st.text_input("Enter Your Name", key="emp_name_input")
    if emp_name:
        emp_row = employees_df[employees_df['name']==emp_name]
        if not emp_row.empty:
            emp_id = emp_row['id'].values[0]
            st.subheader("Pending Visitors")
            visitors_df = get_visitors()
            pending_visitors = visitors_df[(visitors_df['employee_name']==emp_name) &
                                           (visitors_df['status']=='Pending')]
            for idx, row in pending_visitors.iterrows():
                st.write(f"{row['name']} | {row['phone']} | {row['company']} | {row['purpose']} | {row['visit_date_time']}")
                cols = st.columns(3)
                if cols[0].button("Approve", key=f"ap_{row['id']}"):
                    update_visitor_status(row['id'], "Approved")
                    st.experimental_rerun()
                if cols[1].button("Reject", key=f"rj_{row['id']}"):
                    update_visitor_status(row['id'], "Rejected")
                    st.experimental_rerun()
                if cols[2].button("Reschedule", key=f"rs_{row['id']}"):
                    new_dt = st.date_input("New Visit Date", datetime.fromisoformat(row['visit_date_time']).date(), key=f"new_dt_{row['id']}")
                    update_visitor_status(row['id'], "Pending", new_dt.isoformat())
                    st.experimental_rerun()

            # Update availability
            new_status = st.selectbox("Update Status", ["Available","Busy","Not Free Today"],
                                      index=["Available","Busy","Not Free Today"].index(emp_row['status'].values[0]), key="emp_status")
            if st.button("Update Status", key="update_status_btn"):
                update_employee(emp_id, emp_name, emp_row['role'].values[0], emp_row['department'].values[0], new_status)
                st.success("Status Updated")

# ----------------- Admin -----------------
elif role == "Admin":
    st.header("Admin Dashboard")
    visitors_df = get_visitors()
    st.subheader("All Visitors")
    st.dataframe(visitors_df)

    st.subheader("Filter & Download")
    employees_df = get_employees()
    filter_emp = st.selectbox("Filter by Employee", ["All"] + employees_df['name'].tolist())
    filter_date = st.date_input("Filter by Date")
    visitors_df['visit_date_only'] = pd.to_datetime(visitors_df['visit_date_time']).dt.date
    filtered = visitors_df
    if filter_emp != "All":
        filtered = filtered[filtered['employee_name']==filter_emp]
    filtered = filtered[filtered['visit_date_only']==filter_date]
    st.dataframe(filtered)
    csv = filtered.drop(columns=['visit_date_only']).to_csv(index=False).encode('utf-8')
    st.download_button("Download CSV", csv, "visitors.csv", "text/csv")