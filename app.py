import streamlit as st
import sqlite3
import pandas as pd
from datetime import datetime

# -----------------------------
# DATABASE (FRESH SAFE INIT)
# -----------------------------
conn = sqlite3.connect("vms.db", check_same_thread=False)
c = conn.cursor()

c.execute("""
CREATE TABLE IF NOT EXISTS employees (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT,
    role TEXT,
    department TEXT,
    status TEXT DEFAULT 'Available'
)
""")

c.execute("""
CREATE TABLE IF NOT EXISTS visitors (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT,
    phone TEXT,
    company TEXT,
    purpose TEXT,
    employee_id INTEGER,
    visit_date_time TEXT,
    exit_time TEXT,
    duration TEXT,
    status TEXT DEFAULT 'Pending'
)
""")

conn.commit()

# -----------------------------
# SAFE FETCH
# -----------------------------
def get_employees():
    try:
        return pd.read_sql("SELECT * FROM employees", conn)
    except:
        return pd.DataFrame(columns=["id","name","role","department","status"])

def get_visitors():
    try:
        return pd.read_sql("SELECT * FROM visitors", conn)
    except:
        return pd.DataFrame(columns=["id","name","phone","company","purpose","employee_id","visit_date_time","exit_time","duration","status"])

# -----------------------------
# EMPLOYEE FUNCTIONS
# -----------------------------
def add_employee(name, role, dept):
    if name.strip() == "":
        st.warning("Name required")
        return
    c.execute("INSERT INTO employees (name, role, department) VALUES (?, ?, ?)", (name, role, dept))
    conn.commit()

def update_employee(emp_id, name, role, dept, status):
    c.execute("UPDATE employees SET name=?, role=?, department=?, status=? WHERE id=?",
              (name, role, dept, status, emp_id))
    conn.commit()

def delete_employee(emp_id):
    c.execute("DELETE FROM employees WHERE id=?", (emp_id,))
    conn.commit()

# -----------------------------
# VISITOR FUNCTIONS
# -----------------------------
def add_visitor(name, phone, company, purpose, emp_id, visit_dt):
    if name.strip() == "":
        st.warning("Visitor name required")
        return False

    c.execute("""
        INSERT INTO visitors (name, phone, company, purpose, employee_id, visit_date_time)
        VALUES (?, ?, ?, ?, ?, ?)
    """, (name, phone, company, purpose, emp_id, visit_dt))
    conn.commit()
    return True

def mark_exit(visitor_id):
    now = datetime.now().strftime("%Y-%m-%d %H:%M:%S")

    c.execute("SELECT visit_date_time FROM visitors WHERE id=?", (visitor_id,))
    start = c.fetchone()

    duration = ""
    if start and start[0]:
        try:
            duration = str(datetime.fromisoformat(now) - datetime.fromisoformat(start[0]))
        except:
            duration = ""

    c.execute("""
        UPDATE visitors
        SET exit_time=?, duration=?, status='Completed'
        WHERE id=?
    """, (now, duration, visitor_id))
    conn.commit()

