employees_df = get_employees()

if not employees_df.empty:
    for _, row in employees_df.iterrows():
        with st.sidebar.expander(row.get("name","Employee")):

            new_name = st.text_input("Name", row.get("name",""), key=f"n{row['id']}")
            new_role = st.text_input("Role", row.get("role",""), key=f"r{row['id']}")
            new_dept = st.text_input("Dept", row.get("department",""), key=f"d{row['id']}")

            status_options = ["Available","Already in a Meeting","Not Free Today"]
            current_status = row.get("status","Available")

            if current_status not in status_options:
                current_status = "Available"

            new_status = st.selectbox("Status", status_options,
                                     index=status_options.index(current_status),
                                     key=f"s{row['id']}")

            if st.button("Update", key=f"u{row['id']}"):
                update_employee(row["id"], new_name, new_role, new_dept, new_status)
                st.success("Updated")
                st.rerun()

            if st.button("Delete", key=f"x{row['id']}"):
                delete_employee(row["id"])
                st.success("Deleted")
                st.rerun()

