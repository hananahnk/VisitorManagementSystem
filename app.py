import streamlit as st
import sqlite3
from datetime import datetime, timedelta

# ------------------ DATABASE SETUP ------------------
conn = sqlite3.connect("vms.db", check_same_thread=False)
c = conn.cursor()

# Employees table
c.execute('''
CREATE TABLE IF NOT EXISTS employees (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    role TEXT,
    department TEXT,
    status TEXT DEFAULT 'Available'
)
''')

# Visitors table
c.execute('''
CREATE TABLE IF NOT EXISTS visitors (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    phone TEXT,
    company TEXT,
    purpose TEXT,
    employee_id INTEGER,
    visit_date_time TEXT,
    exit_time TEXT,
    duration TEXT,
    status TEXT DEFAULT 'Pending',
    FOREIGN KEY(employee_id) REFERENCES employees(id)
)
''')
conn.commit()

# ------------------ UTILITY FUNCTIONS ------------------

def add_employee(name, role, department):
    c.execute("INSERT INTO employees (name, role, department) VALUES (?, ?, ?)", (name, role, department))
    conn.commit()
    return True

def update_employee(emp_id, name, role, department, status):
    c.execute("UPDATE employees SET name=?, role=?, department=?, status=? WHERE id=?", (name, role, department, status, emp_id))
    conn.commit()

def delete_employee(emp_id):
    c.execute("DELETE FROM employees WHERE id=?", (emp_id,))
    conn.commit()

def add_visitor(name, phone, company, purpose, employee_id, visit_dt):
    c.execute("INSERT INTO visitors (name, phone, company, purpose, employee_id, visit_date_time) VALUES (?, ?, ?, ?, ?, ?)",
              (name, phone, company, purpose, employee_id, visit_dt))
    conn.commit()

def update_visitor_status(visitor_id, status, new_time=None):
    if new_time:
        c.execute("UPDATE visitors SET status=?, visit_date_time=? WHERE id=?", (status, new_time, visitor_id))
    else:
        c.execute("UPDATE visitors SET status=? WHERE id=?", (status, visitor_id))
    conn.commit()

def mark_exit(visitor_id):
    exit_time = datetime.now()
    c.execute("SELECT visit_date_time FROM visitors WHERE id=?", (visitor_id,))
    visit_time_str = c.fetchone()[0]
    visit_time = datetime.fromisoformat(visit_time_str)
    duration = exit_time - visit_time
    c.execute("UPDATE visitors SET exit_time=?, duration=?, status='Completed' WHERE id=?", (exit_time.isoformat(), str(duration), visitor_id))
    conn.commit()

def get_employees():
    c.execute("SELECT * FROM employees")
    rows = c.fetchall()
    return rows

def get_visitors():
    c.execute("SELECT * FROM visitors")
    rows = c.fetchall()
    return rows

