import streamlit as st
import sqlite3
import pandas as pd
from datetime import datetime

# --- Database Setup ---
conn = sqlite3.connect("vms.db", check_same_thread=False)
c = conn.cursor()

# Employees table
c.execute("""
CREATE TABLE IF NOT EXISTS employees (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    role TEXT,
    department TEXT,
    status TEXT DEFAULT 'Available'
)
""")

# Visitors table
c.execute("""
CREATE TABLE IF NOT EXISTS visitors (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT,
    phone TEXT,
    company TEXT,
    purpose TEXT,
    employee_id INTEGER,
    visit_date_time TEXT,
    exit_time TEXT,
    duration TEXT,
    status TEXT DEFAULT 'Pending',
    FOREIGN KEY(employee_id) REFERENCES employees(id)
)
""")
conn.commit()

# --- Helper Functions ---
def load_employees():
    df = pd.read_sql("SELECT * FROM employees", conn)
    # Ensure status column exists
    if 'status' not in df.columns:
        df['status'] = 'Available'
    return df

def load_visitors():
    df = pd.read_sql("SELECT * FROM visitors", conn)
    # Ensure essential columns exist
    for col in ['visit_date_time','exit_time','duration','status']:
        if col not in df.columns:
            df[col] = ''
    return df

def add_employee(name, role, department):
    c.execute("INSERT INTO employees (name, role, department) VALUES (?, ?, ?)", (name, role, department))
    conn.commit()

def update_employee(emp_id, name, role, department, status):
    c.execute("UPDATE employees SET name=?, role=?, department=?, status=? WHERE id=?",
              (name, role, department, status, emp_id))
    conn.commit()

def delete_employee(emp_id):
    c.execute("DELETE FROM employees WHERE id=?", (emp_id,))
    conn.commit()

def add_visitor(name, phone, company, purpose, employee_id, visit_dt):
    c.execute("""INSERT INTO visitors
                 (name, phone, company, purpose, employee_id, visit_date_time)
                 VALUES (?, ?, ?, ?, ?, ?)""",
              (name, phone, company, purpose, employee_id, visit_dt))
    conn.commit()

def approve_visitor(visitor_id):
    c.execute("UPDATE visitors SET status='Approved' WHERE id=?", (visitor_id,))
    conn.commit()

def reject_visitor(visitor_id):
    c.execute("UPDATE visitors SET status='Rejected' WHERE id=?", (visitor_id,))
    conn.commit()

def reschedule_visitor(visitor_id, new_dt):
    c.execute("UPDATE visitors SET visit_date_time=?, status='Pending' WHERE id=?", (new_dt, visitor_id))
    conn.commit()

def mark_exit(visitor_id):
    exit_time = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    visitor = pd.read_sql(f"SELECT visit_date_time FROM visitors WHERE id={visitor_id}", conn)
    visit_dt = visitor.loc[0,'visit_date_time']
    duration = ""
    if visit_dt:
        fmt = "%Y-%m-%d %H:%M:%S"
        try:
            start = datetime.strptime(visit_dt, fmt)
            end = datetime.strptime(exit_time, fmt)
            duration = str(end - start)
        except:
            duration = ""
    c.execute("UPDATE visitors SET exit_time=?, duration=? WHERE id=?", (exit_time, duration, visitor_id))
    conn.commit()

# --- App UI ---
st.title("Visitor Management System")
sector = st.selectbox("Select Sector", ["Receptionist","Employee","Admin"])

employees_df = load_employees()
visitors_df = load_visitors()

# Merge employee names safely
if not visitors_df.empty and not employees_df.empty:
    visitors_df = visitors_df.merge(
        employees_df[['id','name']],
        how='left',
        left_on='employee_id',
        right_on='id',
        suffixes=('','_emp')
    )
    visitors_df.rename(columns={'name_emp':'employee_name'}, inplace=True)
else:
    visitors_df['employee_name'] = ''

# ----------------- Receptionist -----------------
if sector=="Receptionist":
    st.subheader("Add Employee")
    with st.form("add_employee_form"):
        e_name = st.text_input("Name")
        e_role = st.text_input("Role")
        e_dept = st.text_input("Department")
        submitted = st.form_submit_button("Add Employee")
        if submitted and e_name:
            add_employee(e_name, e_role, e_dept)
            st.success("Employee Added")
            st.experimental_rerun()

    st.subheader("Employee Management")
    for _, row in employees_df.iterrows():
        col1,col2,col3,col4 = st.columns([2,2,2,1])
        col1.text_input("Name", value=row['name'], key=f"name{row['id']}")
        col2.text_input("Role", value=row['role'], key=f"role{row['id']}")
        col3.text_input("Department", value=row['department'], key=f"dept{row['id']}")
        if col4.button("Update", key=f"update{row['id']}"):
            name = st.session_state.get(f"name{row['id']}", row['name'])
            role = st.session_state.get(f"role{row['id']}", row['role'])
            dept = st.session_state.get(f"dept{row['id']}", row['department'])
            status = row.get('status','Available')
            update_employee(row['id'], name, role, dept, status)
            st.success("Employee Updated")
            st.experimental_rerun()
        if col4.button("Delete", key=f"del{row['id']}"):
            delete_employee(row['id'])
            st.success("Employee Deleted")
            st.experimental_rerun()

    st.subheader("Register Visitor")
    available_emps = employees_df[employees_df['status']=='Available']
    if available_emps.empty:
        st.warning("No Available Employees")
    else:
        with st.form("add_visitor"):
            v_name = st.text_input("Visitor Name")
            v_phone = st.text_input("Phone")
            v_company = st.text_input("Company")
            v_purpose = st.text_input("Purpose")
            emp_options = available_emps['id'].tolist()
            emp_dict = dict(zip(available_emps['id'], available_emps['name']))
            selected_emp_id = st.selectbox("Select Employee", emp_options, format_func=lambda x: emp_dict[x])
            visit_dt = st.datetime_input("Visit Date & Time", datetime.now())
            submitted = st.form_submit_button("Register Visitor")
            if submitted and v_name:
                add_visitor(v_name, v_phone, v_company, v_purpose, selected_emp_id, visit_dt.strftime("%Y-%m-%d %H:%M:%S"))
                st.success("Visitor Added (Pending Approval)")
                st.experimental_rerun()

    st.subheader("Visitors Dashboard / Mark Exit")
    if not visitors_df.empty:
        st.dataframe(visitors_df[['id','name','phone','company','purpose','employee_name','visit_date_time','exit_time','duration','status']])
        for _, row in visitors_df.iterrows():
            if row['status']=="Approved" and not row['exit_time']:
                if st.button(f"Mark Exit: {row['name']} ({row['employee_name']})", key=f"exit{row['id']}"):
                    mark_exit(row['id'])
                    st.success("Exit Marked")
                    st.experimental_rerun()

