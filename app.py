import streamlit as st
import sqlite3
import pandas as pd
from datetime import datetime, timedelta

st.set_page_config(layout="wide")
st.title("🏢 Visitor Management System")

# -------------------------
# DATABASE SETUP
# -------------------------
conn = sqlite3.connect("vms.db", check_same_thread=False)
c = conn.cursor()

c.execute("""
CREATE TABLE IF NOT EXISTS employees (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    role TEXT,
    department TEXT,
    status TEXT DEFAULT 'Available'
)
""")

c.execute("""
CREATE TABLE IF NOT EXISTS visitors (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    phone TEXT,
    company TEXT,
    purpose TEXT,
    employee_id INTEGER,
    visit_date_time TEXT,
    exit_time TEXT,
    duration TEXT,
    status TEXT DEFAULT 'Pending'
)
""")
conn.commit()

# -------------------------
# HELPER FUNCTIONS
# -------------------------
def get_employees():
    return pd.read_sql("SELECT * FROM employees", conn)

def get_visitors():
    return pd.read_sql("SELECT * FROM visitors", conn)

def add_employee(name, role, dept):
    c.execute("INSERT INTO employees (name, role, department) VALUES (?, ?, ?)", (name, role, dept))
    conn.commit()

def update_employee(emp_id, name, role, dept, status):
    c.execute("UPDATE employees SET name=?, role=?, department=?, status=? WHERE id=?",
              (name, role, dept, status, emp_id))
    conn.commit()

def delete_employee(emp_id):
    c.execute("DELETE FROM employees WHERE id=?", (emp_id,))
    conn.commit()

def add_visitor(name, phone, company, purpose, emp_id, visit_dt):
    c.execute("""
        INSERT INTO visitors (name, phone, company, purpose, employee_id, visit_date_time)
        VALUES (?, ?, ?, ?, ?, ?)
    """, (name, phone, company, purpose, emp_id, visit_dt))
    conn.commit()

def update_visitor_status(visitor_id, status):
    c.execute("UPDATE visitors SET status=? WHERE id=?", (status, visitor_id))
    conn.commit()

def reschedule_visitor(visitor_id, new_dt):
    c.execute("UPDATE visitors SET visit_date_time=?, status='Pending' WHERE id=?",
              (new_dt, visitor_id))
    conn.commit()

def mark_exit(visitor_id):
    exit_time = datetime.now()
    c.execute("SELECT visit_date_time FROM visitors WHERE id=?", (visitor_id,))
    start_time = datetime.strptime(c.fetchone()[0], "%Y-%m-%d %H:%M:%S")
    duration = exit_time - start_time
    c.execute("UPDATE visitors SET exit_time=?, duration=?, status='Completed' WHERE id=?",
              (exit_time.strftime("%Y-%m-%d %H:%M:%S"), str(duration), visitor_id))
    conn.commit()

def get_employee_name(emp_id):
    c.execute("SELECT name FROM employees WHERE id=?", (emp_id,))
    res = c.fetchone()
    return res[0] if res else "Unknown"

# -------------------------
# ROLES
# -------------------------
role = st.sidebar.selectbox("Select Role", ["Receptionist", "Employee", "Admin"])
employees_df = get_employees()
visitors_df = get_visitors()

# =========================================================
# RECEPTIONIST
# =========================================================
if role == "Receptionist":
    st.header("Receptionist Dashboard")

    col1, col2 = st.columns([2,1])

    # --- Visitor Registration ---
    with col1:
        st.subheader("Register Visitor")
        available = employees_df[employees_df["status"]=="Available"] if not employees_df.empty else pd.DataFrame()
        if not available.empty:
            v_name = st.text_input("Visitor Name")
            v_phone = st.text_input("Phone")
            v_company = st.text_input("Company")
            v_purpose = st.text_input("Purpose")
            emp_name = st.selectbox("Assign Employee", available["name"].tolist())
            v_date = st.date_input("Visit Date")
            v_time = st.time_input("Visit Time")

            if st.button("Add Visitor"):
                emp_id = available[available["name"]==emp_name].iloc[0]["id"]
                visit_dt = datetime.combine(v_date, v_time).strftime("%Y-%m-%d %H:%M:%S")
                add_visitor(v_name, v_phone, v_company, v_purpose, emp_id, visit_dt)
                st.success("Visitor Added (Pending Approval)")
        else:
            st.warning("No Available Employees")

    # --- Employee Management ---
    with col2:
        st.subheader("Employee Management")
        with st.expander("Add Employee"):
            ename = st.text_input("Name", key="ename")
            erole = st.text_input("Role", key="erole")
            edept = st.text_input("Department", key="edept")
            if st.button("Add Employee", key="add_emp"):
                add_employee(ename, erole, edept)
                st.success("Employee Added")

        if not employees_df.empty:
            for _, row in employees_df.iterrows():
                with st.expander(row["name"]):
                    new_name = st.text_input("Name", row["name"], key=f"name{row['id']}")
                    new_role = st.text_input("Role", row["role"], key=f"role{row['id']}")
                    new_dept = st.text_input("Department", row["department"], key=f"dept{row['id']}")
                    status = st.selectbox("Status", ["Available","Already in a Meeting","Not Free Today"], key=f"status{row['id']}")
                    col_update, col_delete = st.columns(2)
                    if col_update.button("Update", key=f"update{row['id']}"):
                        update_employee(row["id"], new_name, new_role, new_dept, status)
                        st.success("Employee Updated")
                    if col_delete.button("Delete", key=f"delete{row['id']}"):
                        delete_employee(row["id"])
                        st.warning("Employee Deleted")

    # --- Visitor Dashboard ---
    st.subheader("Visitor Dashboard")
    if not visitors_df.empty:
        visitors_df_disp = visitors_df.copy()
        visitors_df_disp["Employee Name"] = visitors_df_disp["employee_id"].apply(get_employee_name)
        st.dataframe(visitors_df_disp)
        for _, row in visitors_df_disp.iterrows():
            if row["status"]=="Approved":
                if st.button(f"Mark Exit: {row['name']}", key=f"exit{row['id']}"):
                    mark_exit(row["id"])
                    st.success("Exit Time Marked")
    else:
        st.info("No visitors yet.")

# =========================================================
# EMPLOYEE
# =========================================================
elif role=="Employee":
    st.header("Employee Dashboard")
    emp_name = st.text_input("Enter Your Name")
    if emp_name:
        emp = employees_df[employees_df["name"]==emp_name]
        if not emp.empty:
            emp_id = emp.iloc[0]["id"]
            st.selectbox("Set Status", ["Available","Already in a Meeting","Not Free Today"], index=["Available","Already in a Meeting","Not Free Today"].index(emp.iloc[0]["status"]), key="emp_status")
            my_visitors = visitors_df[visitors_df["employee_id"]==emp_id]

            st.subheader("Pending Approvals")
            pending = my_visitors[my_visitors["status"]=="Pending"]
            for _, row in pending.iterrows():
                st.write(f"Visitor: {row['name']} | Purpose: {row['purpose']} | Scheduled: {row['visit_date_time']}")
                col1, col2, col3 = st.columns(3)
                if col1.button("Approve", key=f"appr{row['id']}"):
                    update_visitor_status(row["id"], "Approved")
                    st.experimental_rerun()
                if col2.button("Reject", key=f"rej{row['id']}"):
                    update_visitor_status(row["id"], "Rejected")
                    st.experimental_rerun()
                new_date = col3.date_input("New Date", key=f"d{row['id']}")
                new_time = col3.time_input("New Time", key=f"t{row['id']}")
                if col3.button("Reschedule", key=f"res{row['id']}"):
                    new_dt = datetime.combine(new_date, new_time).strftime("%Y-%m-%d %H:%M:%S")
                    reschedule_visitor(row["id"], new_dt)
                    st.experimental_rerun()

# =========================================================
# ADMIN
# =========================================================
elif role=="Admin":
    st.header("Admin Dashboard")
    st.subheader("All Visitors")
    if not visitors_df.empty:
        visitors_df_disp = visitors_df.copy()
        visitors_df_disp["Employee Name"] = visitors_df_disp["employee_id"].apply(get_employee_name)
        emp_filter = st.selectbox("Filter by Employee", ["All"]+employees_df["name"].tolist())
        filtered = visitors_df_disp
        if emp_filter!="All":
            emp_id = employees_df[employees_df["name"]==emp_filter].iloc[0]["id"]
            filtered = visitors_df_disp[visitors_df_disp["employee_id"]==emp_id]
        st.dataframe(filtered)
        st.download_button("Download CSV", filtered.to_csv(index=False), "visitors.csv")
    else:
        st.info("No visitors yet.")