        selected_emp = st.selectbox("Assign To Employee", emp_options)
        visit_dt = st.date_input("Visit Date", datetime.now().date())
        visit_time = st.time_input("Visit Time", datetime.now().time())
        visit_datetime = datetime.combine(visit_dt, visit_time).strftime("%Y-%m-%d %H:%M:%S")

