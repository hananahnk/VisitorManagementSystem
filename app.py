        visitor_data = visitors_df[['id','name','phone','company','purpose','employee_name','visit_date_time','exit_time','duration','status']]
        st.dataframe(visitor_data)

        st.subheader("Mark Exit")
        for idx, row in visitors_df.iterrows():
            if row['status'] == "Approved":
                if st.button(f"Mark Exit for {row['name']}"):
                    mark_exit(row['id'])
                    st.success(f"Exit marked for {row['name']}")


